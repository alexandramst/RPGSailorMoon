package LabirintoLunar.Enum;

/**
 * Enumeração que representa os níveis de dificuldade do jogo.
 */
public enum DificuldadeJogo {
    /**
     * Jogo com dificuldade fácil — inimigos mais fracos, mais recompensas.
     */
    FACIL,

    /**
     * Jogo com dificuldade difícil — inimigos mais fortes, menos recompensas.
     */
    DIFICIL
}