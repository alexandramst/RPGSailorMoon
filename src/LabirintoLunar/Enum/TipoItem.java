package LabirintoLunar.Enum;

/**
 * Enumeração que representa os tipos de itens disponíveis no jogo.
 */
public enum TipoItem {
    /**
     * Armas principais equipáveis pelas heroínas.
     */
    ARMA,

    /**
     * Poções que curam ou aumentam atributos temporariamente.
     */
    POCAO,

    /**
     * Consumíveis que causam dano imediato aos inimigos.
     */
    CONSUMIVEL_COMBATE
}